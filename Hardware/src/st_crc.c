void st_crc_init(void)
{
	__HAL_RCC_CRC_CLK_ENABLE();	
	hcrc.Instance = CRC;
  HAL_CRC_Init(&hcrc);
}

/*==================================================================================
* 函 数 名： st_crc32
* 参    数： None
* 功能描述:  st crc校验
* 返 回 值： none
* 备    注： 
* 作    者：  
* 创建时间： 2019-10-16 162442
==================================================================================*/ 
uint32_t st_crc32(uint8_t *buff, uint16_t len)
{ 
	//复位
	CRC->CR = CRC_CR_RESET;
	
	for(int i=0; i<len; i++)
	{
		CRC->DR = buff[i];
	} 
	
	return (CRC->DR);
}

/*********************************************************
// 函数名称：CRC32DataCheckFun
// 函数功能：数据CRC32校验函数
// 入口参数：checkDataBuf：待校验的数组；checkDataLen：待校验的数据长度
// 出口参数：返回值：CRC32校验码
// 注    释：
*********************************************************/
uint32_t CRC32DataCheckFun(uint8_t *checkDataBuf, uint16_t checkDataLen)
{
  uint32_t crc32 = 0;
	st_crc_init();
	crc32 = st_crc32(checkDataBuf,checkDataLen); 
	//debug_print("crc32 = %d\r\n",crc32);
	return crc32;
}
 

